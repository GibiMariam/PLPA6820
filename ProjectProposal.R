#Project proposal
#Data Source and Description
#The data that will be used in this project is generated from a controlled rainfall simulation experiment designed to quantify phosphorus (P) losses under different management conditions. The dataset includes measurements of dissolved reactive P (DRP) and total P collected after each rainfall cycle. The experiment consists of 12 treatments arranged in a completely randomized design (CRD) with four replications. All data generated as part of my own experimental research.  
#Data Volume
#It contains a total of 144 observations (12 treatments × 4 replications × 3 rainfall cycles). The dataset includes five variables, including rainfall cycles, treatments, replication and two response variables (DRP and TP). 
#Research question
#•	How do different treatments influence P losses during rainfall events? 
#  •	Does the P runoff concentration change after each rainfall cycle?
#  •	What will be its impact on water quality? 
#  Data Analysis 
